/**
 * Common MIME types
 */
export enum MimeType {
    /**
     * HTML resource
     */
    HTML = 'text/html',

    /**
     * CSS resource
     */
    CSS = 'text/css',

    /**
     * JavaScript resource
     */
    JAVASCRIPT = 'text/javascript',

    /**
     * JSON resource
     */
    JSON = 'application/json', // eslint-disable-line no-shadow

    /**
     * Plain text resource
     */
    TEXT = 'text/plain'
}